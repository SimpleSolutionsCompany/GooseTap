import 'package:equatable/equatable.dart';
import 'package:json_annotation/json_annotation.dart';

// part "tokken_model.g.dart";

@JsonSerializable()
class Tokken extends Equatable {
  final String tokken;
  final String refreshTokken;

  const Tokken({required this.tokken, required this.refreshTokken});

  Map<String, dynamic> toJson() {
    return {"tokken": tokken, "refreshTokken": refreshTokken};
  }

  factory Tokken.fromJson(Map<String, dynamic> json) {
    return Tokken(tokken: json["tokken"], refreshTokken: json['refreshTokken']);
  }

  @override
  List<Object?> get props => [tokken, refreshTokken];
}
